package Lab2.graph;

public class Triangle {
	
	private Coords a1;
	private Coords a2;
	private Coords a3;
	public Coords getA1() {
		return a1;
	}
	public Coords getA2() {
		return a2;
	}
	public Coords getA3() {
		return a3;
	}
	public Triangle(Coords a1, Coords a2, Coords a3) {
		super();
		this.a1 = a1;
		this.a2 = a2;
		this.a3 = a3;
	}
	
	

}
